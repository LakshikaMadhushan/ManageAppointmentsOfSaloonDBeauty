    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            Customer c1=new Customer();
            c1.setCus_ID(txtCustomerId.getText());
            c1.setCus_Name(txtCustomerName.getText());
            c1.setCus_Address(txtCustomerAddress.getText());
            c1.setCus_TP(Integer.parseInt(txtCustomerNo.getText()));
            boolean isSaved= new CustomerController().saveCustomer(c1);
            if(isSaved){
                JOptionPane.showMessageDialog(null,"Saved Successfully");loadAllCustomer();
            }else{
                 JOptionPane.showMessageDialog(null, "Try again","Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
           // Logger.getLogger(CustomerPanel.class.getName()).log(Level.SEVERE, null, ex);
           JOptionPane.showMessageDialog(null, "Something went wrong","Error", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_jButton5ActionPerformed

    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
        int row = tbl.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) tbl.getModel();
        
       
        txtCustomerId.setText((String) model.getValueAt(row, 0));
         txtCustomerName.setText((String) model.getValueAt(row, 1));
          txtCustomerAddress.setText((String) model.getValueAt(row, 2));
           txtCustomerNo.setText( model.getValueAt(row, 3)+"");
    }//GEN-LAST:event_tblMouseClicked

