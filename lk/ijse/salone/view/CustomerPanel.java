    private void loadAllCustomer() throws SQLException, ClassNotFoundException {
      ArrayList<Customer>list= new CustomerController().getCustomers();
        
        DefaultTableModel model = (DefaultTableModel) tbl.getModel();
         model.setRowCount(0);
        for (Customer customer : list) {
            Object[]rowData={
            customer.getCus_ID(),customer.getCus_Name(),customer.getCus_Address(),customer.getCus_TP()
            
            };
            model.addRow(rowData);
        }
      
      
    }
